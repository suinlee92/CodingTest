package kr.co.codingTest.suin.stackQueue;

public class PatientObj {
    int key;
    int value;

    public PatientObj(int key, int value) {
        this.key = key;
        this.value = value;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
